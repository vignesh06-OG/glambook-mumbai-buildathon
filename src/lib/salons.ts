export function formatPrice(amount: number): string {
  return new Intl.NumberFormat("en-IN", {
    style: "currency",
    currency: "INR",
    maximumFractionDigits: 0,
  }).format(amount);
}

export function priceLevelLabel(level: 1 | 2 | 3): string {
  return "₹".repeat(level) + "·".repeat(3 - level);
}
